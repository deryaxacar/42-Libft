#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*pntr;

	pntr = malloc(size * nmemb);
	if (!(pntr))
		return (NULL);
	ft_bzero(pntr, (nmemb * size));
	return (pntr);
}
